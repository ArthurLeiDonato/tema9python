nome = "lol"

# Acessando por índice (começa em 0)
print(f"Primeira letra: {nome[0]}")   # B
print(f"Última letra: {nome[-1]}")    # o

# Fatiamento [inicio:fim] (fim não incluso)
print(f"Primeiras 3 letras: {nome[0:3]}")  # Bel
print(f"Do índice 2 ao 5: {nome[2:5]}")    # ltr

# Últimos caracteres
print(f"Últimas 3 letras: {nome[-3:]}")    # ano